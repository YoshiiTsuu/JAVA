package exerciciosOrientacaoObjeto;

public class Eletronico {

	private String eletronicoNome;
	private int tamanhoAltura;
	private int tamanhoLargura;
	private String marca;

	public String getEletronicoNome() {
		return eletronicoNome;
	}

	public void setEletronicoNome(String eletronico) {
		this.eletronicoNome = eletronico;
	}

	public int getTamanhoAltura() {
		return tamanhoAltura;
	}

	public void setTamanhoAltura(int altura) {
		this.tamanhoAltura = altura;
	}

	public int getTamanhoLargura() {
		return tamanhoLargura;
	}

	public void setTamanhoLargura(int largura) {
		this.tamanhoLargura = largura;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}
}
